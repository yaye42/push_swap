#include "libft.h"

void	ft_printbits(unsigned char c)
{
	int		a;

	a = 128;
	while (a > 0)
	{
		ft_putchar(c / a + '0');
		c = c % a;
		a = a / 2;
		if (a == 8)
			ft_putchar(' ');
	}
}
